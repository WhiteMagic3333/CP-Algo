typedef long long ll;
#define mod 1000000007
vector<bool> prime;

void sieve(ll n)
{
	prime.resize(n + 1, true);
	prime[0] = prime[1] = false;
	for (ll i = 2; i <= n; i++) {
		if (prime[i] && i * i <= n) {
			for (ll j = i * i; j <= n; j += i)
				prime[j] = false;
		}
	}
}

template<typename T>
std::istream& operator>>(std::istream&in, std::vector<T>& v) {
	for (T& x : v)
		in >> x;
	return in;
}

template<typename T>
std::ostream& operator<<(std::ostream&out, std::vector<T>& v) {
	for (const T& x : v)
		std::cout << x << ' ';
	std::cout << '\n';
	return out;
}

void solve()
{
	int n;
	cin >> n;
	set<array<char, 2>> s;
	string str;
	cin >> str;
	array<char, 2> prev, cur;
	for (int i = 3; i < n; i++)
	{
		prev[0] = str[i - 3];
		prev[1] = str[i - 2];
		cur[0] = str[i - 1];
		cur[1] = str[i];
		s.insert(prev);
		if(s.find(cur) != s.end())
			return void(cout << "YES");
	}
	cout << "NO";
}

