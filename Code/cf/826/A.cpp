using namespace std;
typedef long long ll;
#define mod 1000000007
vector<bool> prime;

void sieve(ll n)
{
	prime.resize(n + 1, true);
	prime[0] = prime[1] = false;
	for (ll i = 2; i <= n; i++) {
		if (prime[i] && i * i <= n) {
			for (ll j = i * i; j <= n; j += i)
				prime[j] = false;
		}
	}
}

template<typename T>
std::istream& operator>>(std::istream&in, std::vector<T>& v) {
	for (T& x : v)
		in >> x;
	return in;
}

template<typename T>
std::ostream& operator<<(std::ostream&out, std::vector<T>& v) {
	for (const T& x : v)
		std::cout << x << ' ';
	std::cout << '\n';
	return out;
}

int score(string &a)
{
	if(a[0] == 'M')
		return 0;
	int cnt = 1 + count(a.begin(), a.end(), 'X');
	if(a.back() == 'S')
		cnt = cnt * (-1);
	return cnt;
}


void solve()
{
	string a, b;
	cin >> a >> b;
	ll sa = score(a), sb = score(b);
	if (sa < sb)
		cout << '<';
	else if (sa > sb)
		cout << '>';
	else
		cout << '=';
}

